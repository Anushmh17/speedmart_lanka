    if (email.isEmpty) { _showError('Please enter your email.'); return; }
    if (password.isEmpty) { _showError('Please enter your password.'); return; }

    setState(() => _isVendorLoading = true);
    try {
      // Read the previously stored flag BEFORE this login
      final wasRemembered = await StorageService.getVendorRememberMe();

      final verifiedUser = await ref.read(authProvider.notifier).verifyVendorCredentials(
        email: email,
        password: password,
      );
      if (!mounted) return;
      final authState = ref.read(authProvider);
      if (authState.hasError || verifiedUser == null) {
        _showError(authState.error ?? 'Login failed.');
        return;
      }
      // Save the new checkbox state for future logins / logout popup
      await StorageService.saveVendorRememberMe(rememberMe);
      // Skip OTP only if vendor had Remember Me from a PREVIOUS session
      if (rememberMe && wasRemembered) {
        await _onVendorOtpSuccess('');
