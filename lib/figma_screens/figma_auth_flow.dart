  Future<void> _onVendorForgotPasswordSendCode(_FigmaAuthPage otpPage) async {
    final email = _forgotPasswordEmailCtrl.text.trim();
    if (email.isEmpty || !email.contains('@')) {
      _showError('Please enter a valid email address.');
      return;
    }
    setState(() => _isVendorLoading = true);
    try {
      final otp = await ref.read(authProvider.notifier).generateResetOtp(email);
      if (!mounted) return;
      _resetOtp = otp;
      _resetEmail = email;
      _previousPassword = await ref.read(authProvider.notifier).getVendorPassword(email);
      _go(otpPage);
    } catch (e) {
      if (mounted) _showError(e.toString().replaceAll('Exception: ', ''));
    } finally {
      if (mounted) setState(() => _isVendorLoading = false);
    }
  }

  void _onVendorForgotPasswordVerifyOtp(
      String enteredOtp, _FigmaAuthPage newPasswordPage) {
    if (_resetOtp == null || enteredOtp != _resetOtp) {
      _showError('Invalid OTP. Please try again.');
      return;
    }
    _go(newPasswordPage);
  }

  Future<void> _onVendorUpdatePassword(
      TextEditingController passwordCtrl,
      TextEditingController confirmCtrl,
      _FigmaAuthPage loginPage) async {
    final password = passwordCtrl.text;
    final confirm = confirmCtrl.text;
    if (password.length < 8) {
      _showError('Password must be at least 8 characters.');
      return;
    }
    if (password != confirm) {
      _showError('Passwords do not match.');
      return;
    }
    if (_resetEmail == null) {
      _showError('Session expired. Please restart the reset process.');
      return;
    }
    setState(() => _isVendorLoading = true);
    try {
      await ref.read(authProvider.notifier).resetPassword(
            email: _resetEmail!,
            newPassword: password,
          );
      if (!mounted) return;
      final authState = ref.read(authProvider);
      if (authState.hasError) {
        _showError(authState.error!);
        return;
      }
      _resetOtp = null;
      _resetEmail = null;
      _previousPassword = null;
      _forgotPasswordEmailCtrl.clear();
      _newPasswordCtrl.clear();
      _confirmNewPasswordCtrl.clear();
      _showToast('Password updated successfully!');
      _go(loginPage);
    } catch (e) {
      if (mounted) _showError(e.toString().replaceAll('Exception: ', ''));
    } finally {
      if (mounted) setState(() => _isVendorLoading = false);
    }
  }

  Future<void> _onVendorOtpSuccess(String otp) async {
    final email = _vendorLoginEmailCtrl.text.trim();
    final password = _vendorLoginPasswordCtrl.text;
    setState(() => _isVendorLoading = true);
