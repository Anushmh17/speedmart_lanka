  Future<void> _onVendorCreateAccount(Map<String, String> data) async {
    final email = data['email'] ?? '';
    final fullName = data['fullName'] ?? '';
    final password = data['password'] ?? '';
    final confirmPassword = data['confirmPassword'] ?? '';
    final shopName = data['shopName'] ?? '';
    final categories = data['categories'] ?? '';

    if (fullName.trim().isEmpty) { _showError('Please enter your full name.'); return; }
    if (email.trim().isEmpty || !email.contains('@')) { _showError('Please enter a valid email.'); return; }
    if (password.length < 8) { _showError('Password must be at least 8 characters.'); return; }
    if (password != confirmPassword) { _showError('Passwords do not match.'); return; }
    if (shopName.trim().isEmpty) { _showError('Please enter your shop name.'); return; }
    if (categories.trim().isEmpty) { _showError('Please select at least one category.'); return; }

    final lat = double.tryParse(data['latitude'] ?? '');
    final lng = double.tryParse(data['longitude'] ?? '');
    if (lat == null || lng == null) { _showError('Please pin your shop location on the map.'); return; }

    setState(() => _isVendorLoading = true);
    try {
      final isSriLanka = data['country'] == 'Sri Lanka';
      await ref.read(authProvider.notifier).register(
        fullName: fullName.trim(),
        email: email.trim(),
        phone: data['phone'] ?? '',
        password: password,
        role: UserRole.vendor,
        businessName: shopName.trim(),
        categories: categories.isNotEmpty ? categories.split(',') : null,
        shopName: shopName.trim(),
        shopAddress: data['shopAddress'] ?? '',
        shopProvince: data['province']?.isNotEmpty == true ? data['province'] : null,
        shopDistrict: data['district']?.isNotEmpty == true ? data['district'] : null,
        shopLatitude: lat,
        shopLongitude: lng,
        shopLocationSource: 'map_pin',
        businessRegistrationNumber: data['businessRegNo']?.isNotEmpty == true ? data['businessRegNo'] : null,
        detectedCountry: isSriLanka ? 'LK' : 'OTHER',
        selectedCountry: isSriLanka ? 'LK' : 'OTHER',
      );
      if (!mounted) return;
      final authState = ref.read(authProvider);
      if (authState.hasError) {
        _showError(authState.error!);
      } else if (authState.isAuthenticated) {
        context.go(RouteNames.vendorHome);
      }
    } catch (e) {
      if (mounted) _showError(e.toString().replaceAll('Exception: ', ''));
    } finally {
      if (mounted) setState(() => _isVendorLoading = false);
    }
  }

