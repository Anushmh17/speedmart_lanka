  @override
  Future<bool> didPopRoute() async {
    if (_isHandlingBack) return true;
    _isHandlingBack = true;
    try {
      final allow = await _onWillPop();
      if (allow && mounted) {
        SystemNavigator.pop();
      }
    } finally {
      _isHandlingBack = false;
    }
    return true;
  }



  void _go(_FigmaAuthPage page, {bool back = false, bool postFrame = false}) {
    if (!mounted) return;
    if (postFrame) {
      WidgetsBinding.instance.addPostFrameCallback((_) => _go(page, back: back));
      return;
    }
    FocusScope.of(context).unfocus();
    final currentIdx = _pageOrder.indexOf(_page);
    final nextIdx = _pageOrder.indexOf(page);
    final goingBack = back || (nextIdx < currentIdx);
    _dirNotifier.value = goingBack ? -1.0 : 1.0;
    setState(() {
      _prevPage = _page;
      _page = page;
    });
    _animCtrl.forward(from: 0.0);
  }

