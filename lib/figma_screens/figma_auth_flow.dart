  // ── GPS auto-detect ───────────────────────────────────────────────────────

  Future<void> _autoDetectLocation() async {
    setState(() => _isDetectingGps = true);
    try {
      final position = await GpsLocationService().getCurrentPosition();
      final result = await ReverseGeocodingService().reverseGeocode(
        latitude: position.latitude,
        longitude: position.longitude,
      );
      if (!mounted) return;
      if (result.geocodingSucceeded &&
          result.province != null &&
          result.district != null) {
        final province = SriLankaData.provinceByName(result.province!);
        final district = SriLankaData.districtByName(result.district!);
        if (province != null && district != null) {
          setState(() {
            _selectedProvince = province;
            _selectedDistrict = district;
            _detectedLatitude = position.latitude;
            _detectedLongitude = position.longitude;
          });
          ref.read(customerRegistrationProvider.notifier).applyGpsLocation(
                province: province,
                district: district,
                approxArea: result.city ?? '',
              );
          if (mounted) {
            _showToast('Location detected: ${district.name}, ${province.name}');
          }
          return;
        }
      }
      if (mounted) _showToast('Could not detect location. Please select manually.');
    } on LocationException catch (e) {
      if (mounted) _showToast(e.message, isError: true);
    } catch (_) {
      if (mounted) _showToast('GPS unavailable. Please select manually.', isError: true);
    } finally {
      if (mounted) setState(() => _isDetectingGps = false);
    }
  }

  Future<void> _autoDetectLocationInternational() async {
    setState(() => _isDetectingGps = true);
    try {
      final position = await GpsLocationService().getCurrentPosition();
      if (!mounted) return;
      setState(() {
        _detectedLatitude = position.latitude;
        _detectedLongitude = position.longitude;
      });
      _showToast('Location detected');
    } on LocationException catch (e) {
      if (mounted) _showToast(e.message, isError: true);
    } catch (_) {
      if (mounted) _showToast('GPS unavailable. Please try again.', isError: true);
    } finally {
      if (mounted) setState(() => _isDetectingGps = false);
    }
  }

  // ── Customer register: validate & send OTP ────────────────────────────────

  Future<void> _onSriLankaCustomerCreateAccount(
      Map<String, String> data) async {
    final phone = data['phone'] ?? '';
    final fullName = data['fullName'] ?? '';
    final nic = data['nic'] ?? '';
    final preciseAddress = data['preciseAddress'] ?? '';

    if (fullName.trim().isEmpty) {
      _showError('Please enter your full name.');
      return;
    }
    if (phone.trim().isEmpty) {
      _showError('Please enter your phone number.');
      return;
    }
    if (_selectedProvince == null) {
      _showError('Please select your Province.');
      return;
    }
    if (_selectedDistrict == null) {
      _showError('Please select your District.');
      return;
    }

    final reg = ref.read(customerRegistrationProvider.notifier);
    reg.setMode(isLogin: false);
    reg.updateFullName(fullName.trim());
    reg.updatePhone(phone.trim());
    reg.updateNic(nic.trim());
    reg.updatePreciseAddress(preciseAddress.trim());
    reg.updateDeliveryNote(data['deliveryNote'] ?? '');
    reg.updateEmail(data['email'] ?? '');
    reg.updateProvince(_selectedProvince);
    reg.updateDistrict(_selectedDistrict);

    await reg.sendOtp();

    if (!mounted) return;
    final state = ref.read(customerRegistrationProvider);
    if (state.hasError) {
      _showError(state.error!);
      return;
    }
    _go(_FigmaAuthPage.sriLankaCustomerRegisterOtp);
  }

  Future<void> _onInternationalCustomerCreateAccount(
      Map<String, String> data) async {
