  // ── OTP success handlers ──────────────────────────────────────────────────

  Future<void> _onCustomerLoginOtpSuccess(String otp) async {
    // Real OTP verification via provider
    final ok = await ref.read(customerRegistrationProvider.notifier).verifyOtp(otp);
    if (!mounted) return;
    if (!ok) {
      _showError(ref.read(customerRegistrationProvider).error ?? 'Incorrect OTP. Please try again.');
      return;
    }
    final regState = ref.read(customerRegistrationProvider);
    final contact = regState.data.primaryContact;
    try {
      await ref.read(authProvider.notifier).loginCustomerOtp(contact: contact);
      if (!mounted) return;
      context.go(RouteNames.customerHome);
    } catch (e) {
      if (!mounted) return;
      _showError(e.toString().replaceAll('Exception: ', ''));
    }
  }

  Future<void> _onCustomerRegisterOtpSuccess(String otp) async {
    // Real OTP verification via provider
    final ok = await ref.read(customerRegistrationProvider.notifier).verifyOtp(otp);
    if (!mounted) return;
    if (!ok) {
      _showError(ref.read(customerRegistrationProvider).error ?? 'Incorrect OTP. Please try again.');
      return;
    }
    final regState = ref.read(customerRegistrationProvider);
    try {
      await ref.read(authProvider.notifier).register(
            fullName: regState.data.fullName,
            email: regState.data.email,
            phone: regState.data.phone,
            password: '',
            role: UserRole.customer,
            nic: regState.data.nic,
            detectedCountry: regState.data.overrideInfo.detectedCountry,
            selectedCountry: regState.data.overrideInfo.selectedCountry,
            countryOverride: regState.data.overrideInfo.isCountryOverride,
            detectionSource: regState.data.overrideInfo.detectionSource,
            riskFlag: regState.data.overrideInfo.riskFlag,
            verifiedPhone: regState.data.overrideInfo.verifiedPhone || regState.data.isLkUser,
            verifiedEmail: regState.data.overrideInfo.verifiedEmail || !regState.data.isLkUser,
            deliveryCountry: regState.data.country,
            deliveryProvince: _selectedProvince?.name ?? regState.data.province?.name,
            deliveryDistrict: _selectedDistrict?.name ?? regState.data.district?.name,
            deliveryApproxArea: regState.data.approxArea,
            deliveryPreciseAddress: regState.data.preciseAddress,
            deliveryNote: regState.data.deliveryNote,
          );
      if (!mounted) return;
      // Save default delivery address so customer home can use it immediately
      final user = ref.read(currentUserProvider);
      if (user != null) {
        final defaultAddress = CustomerDeliveryAddress.fromUserFields(
          customerId: user.id,
          deliveryProvince: _selectedProvince?.name ?? regState.data.province?.name,
          deliveryDistrict: _selectedDistrict?.name ?? regState.data.district?.name,
          deliveryApproxArea: regState.data.approxArea,
          deliveryPreciseAddress: regState.data.preciseAddress,
          deliveryNote: regState.data.deliveryNote,
        );
        await ref
            .read(customerDeliveryAddressProvider.notifier)
            .saveDefaultAddress(defaultAddress);
      }
      if (!mounted) return;
      context.go(RouteNames.customerHome);
    } catch (e) {
      if (!mounted) return;
      _showError(e.toString().replaceAll('Exception: ', ''));
    }
  }

