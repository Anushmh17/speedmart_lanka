  @override
  void initState() {
    super.initState();
    _animCtrl = AnimationController(
      vsync: this,
      duration: const Duration(milliseconds: 340),
    );
    // Build direction-aware animations once using a curved parent
    final curved = CurvedAnimation(parent: _animCtrl, curve: Curves.easeInOutCubic);
    _inSlide = _DirectionalTween(
      dirNotifier: _dirNotifier,
      inbound: true,
    ).animate(curved);
    _outSlide = _DirectionalTween(
      dirNotifier: _dirNotifier,
      inbound: false,
    ).animate(curved);
    _animCtrl.value = 1.0;
    _detectCountry();
  }

  @override
  void dispose() {
    _animCtrl.dispose();
    _regPhoneCtrl.dispose();
    _regEmailCtrl.dispose();
    _loginPhoneCtrl.dispose();
    _loginEmailCtrl.dispose();
    _vendorLoginEmailCtrl.dispose();
    _vendorLoginPasswordCtrl.dispose();
    _forgotPasswordEmailCtrl.dispose();
    _newPasswordCtrl.dispose();
    _confirmNewPasswordCtrl.dispose();
    super.dispose();
  }



  void _go(_FigmaAuthPage page, {bool back = false, bool postFrame = false}) {
    if (!mounted) return;
    if (postFrame) {
      WidgetsBinding.instance.addPostFrameCallback((_) => _go(page, back: back));
      return;
    }
    FocusScope.of(context).unfocus();
    final currentIdx = _pageOrder.indexOf(_page);
    final nextIdx = _pageOrder.indexOf(page);
    final goingBack = back || (nextIdx < currentIdx);
    _dirNotifier.value = goingBack ? -1.0 : 1.0;
    setState(() {
      _prevPage = _page;
      _page = page;
    });
    _animCtrl.forward(from: 0.0);
  }

